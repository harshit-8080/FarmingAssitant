<p style="font-size: 50px;margin-left: 30px;">Introduction</p>
<div id="d1"   style="font-size:20px">Agriculture is counted as the chief economic occupation of the state. According to a government estimate, net sown area of the state is 4.828 million hectares and the gross sown area is 5.788 million hectares.[32] Horticulture and animal husbandry also engage a major share of the total population of the state.[33] About 80% of the population of the state is rural and the main livelihood of the villagers is agriculture and agriculture-based small industry.

The majority of the farmers are still practising the traditional methods of cultivation, resulting in low growth rates and productivity. The farmers have to be made aware of modern technologies suitable to their holdings. Providing adequate knowledge to the farmers is essential for better implementation of the agricultural development plans and to improve the productivity.[34]


