<p style ="font-size: 50px;margin-left: 30px;">Climate of Chattisgarh</p>
<div id="d1" style="font-size: 20px">The climate of Chhattisgarh is tropical. It is hot and humid because of its proximity to the Tropic of Cancer and its dependence on the monsoons for rains. Summer temperatures in Chhattisgarh can reach 45 °C (113 °F).[18] The monsoon season is from late June to October and is a welcome respite from the heat. Chhattisgarh receives an average of 1,292 millimetres (50.9 in) of rain. Winter is from November to January and it is a good time to visit Chhattisgarh. Winters are pleasant with low temperatures and less humidity.</div>



