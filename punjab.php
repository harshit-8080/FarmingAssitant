<p style ="font-size: 50px;margin-left: 30px;">Climate of Panjab</p>
<div id="d1" style="font-size: 20px"> The geography and subtropical latitudinal location of Punjab lead to large variations in temperature from month to month. Even though only limited regions experience temperatures below 0 °C (32 °F), ground frost is commonly found in the majority of Punjab during the winter season. The temperature rises gradually with high humidity and overcast skies. However, the rise in temperature is steep when the sky is clear and humidity is low.[45]

The maximum temperatures usually occur in mid-May and June. The temperature remains above 40 °C (104 °F) in the entire region during this period. Ludhiana recorded the highest maximum temperature at 46.1 °C (115.0 °F) with Patiala and Amritsar recording 45.5 °C (113.9 °F). The maximum temperature during the summer in Ludhiana remains above 41 °C (106 °F) for a duration of one and a half months. These areas experience the lowest temperatures in January. The sun rays are oblique during these months and the cold winds control the temperature at daytime.[45]</div>



