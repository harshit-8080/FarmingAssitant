<div id="d1" style="font-size: 20px"> he state has five seasons: winter, summer, spring, autumn, and a monsoon season between June and September. Sikkim's climate ranges from sub-tropical in the south to tundra in the north. Most of the inhabited regions of Sikkim experience a temperate climate, with temperatures seldom exceeding 28 °C (82 °F) in summer.</div>



