<p style ="font-size: 50px;margin-left: 30px;">Important agriculture crops of Kerala</p>
<div id="d1" style="font-size: 20px">Rice is the most important food crop grown in Kerala. It occupies 7.46 percent of the total cropped area of the state. However, the area under rice has been falling at an alarming rate ever since the 1980s. From 8.82 lakh hectare in 1974-75, the paddy area has come down to 1.96 lakh hectare in 2015-16. The production has also concomitantly declined from 13.76 lakh MT in 1972-73 (peak of production) to 5.49 lakh MT in 2015-16 (Appendix 2.4). Moreover, the productivity of the crop is very low in the State (2790 kg/ha), though it is higher than the national average (2424 kg/ha). 
