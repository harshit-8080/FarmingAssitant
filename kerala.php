The State has witnessed a remarkable transformation in the agricultural sector since its formation. The implementation of Land Reforms Act in 1963 became the knell of feudalism and it became the landmark in the Kerala's agricultural sector and a stepping-stone for further reforms in this field.

Cash Crops like coconut, rubber, tea, coffee, pepper, cardamom, areca nut, ginger, nutmeg, cinnamon etc. and food crops paddy tapioca etc. gives the agricultural sector of Kerala a distinct flavour. The agro climatic conditions in Kerala suite the cultivation of a variety of seasonal and perennial crops. Fifteen principle crops (Rice, pulses, coconut, rubber, tea, coffee, pepper, cardamom, areca nut, ginger, nutmeg, cinnamon, paddy tapioca and other plantations) are cultivated from the net areas sown of 21,11,471 hector in the State.
