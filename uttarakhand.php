    <h1>Irrigation</h1>
    <p>
       In the state, farmers adopt generally two types of agricultural practices i.e. the rainfed and the irrigated. Most of the agriculture in the state is rainfed. The net irrigated area of the state stands at 3.38 lakh hectares (2009-2010). The net irrigated area to net sown area for the state is 45 percent. Being large area under hills, irrigation is available mostly in the plains and valleys. So there is a need to generate alternate sources of irrigation to increase the net irrigated area, which in turn shall also increase the cropping intensity of the state. These alternative sources can be rainwater harvesting, check dams, hydram for lift irrigation etc. Technologies like drip irrigation, sprinklers etc. can also be used for better water management. 
    </p>
