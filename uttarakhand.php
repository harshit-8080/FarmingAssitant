    <h1>Overview</h1>
    <p>
      Uttarakhand being a hill state offers great opportunity in agriculture and animal husbandry, about 75% of the people living in Uttarakhand are dependent on agriculture for their livelihood. While those residing in the vulnerable parts are untouched by modernization so they lack knowledge about the new agricultural techniques which results in less produce. However, educating the agro-based communities about hybrid seeds, machinery, bio-techniques, organic farming, haphazard of insecticides and pesticides etc. can help them in raising their productivity.
    </p>
    <p>
      Most of the area of State is under forests and wastelands thus leaving only a small amount of land i.e. 7.41lakh ha (about 14%) (LUS 2009-10)  for cultivation out of the total reported area of 56.72 lakh ha.
    </p>
    <p>
      Out of the total, about 89% are under small and sub marginal. As large number and area is under small and marginal holdings, scale of economies cannot be availed of, and so the input cost per unit of output is higher. The soil of tarai region is very fertile and support to number of crops. Indiscriminate use of chemicals and overexploitation of groundwater makes the soil of this region less fertile, causing the reduction in the sustainability in productivity. On the other hand the hill region is prone to constant soil erosion due to steep slopes making it less and less fertile, which could be achieved through adoption of better management practices.
    </p>
