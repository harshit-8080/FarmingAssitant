    <h1>Major Crops</h1>
    <p>
     Like most of India, agriculture is one of the most significant sectors of the economy of Uttarakhand. Basmati rice, wheat, soybeans, groundnuts, coarse cereals, pulses, and oil seeds are the most widely grown crops.  
    </p>
    <h2 style="color: blue">Rice</h2>
    <p>
      The state has 0.3 million ha under rice cultivation which covers irrigated and rainfed hill rice areas. The current state average productivity is about 2 tonnes/ha and the major constraints in production are unsustainable intensive rice-wheat production systems in tarai areas, decreasing organic carbon content in soils, imbalanced fertilizer use, biotic stresses due to weeds, bacterial leaf blight, stem borer (tarai) and blast (hill area).
    </p>
    <h2 style="color: blue">Wheat</h2>
    <p>
      Uttarakhand consists of hilly tracts as well as tarai areas where wheat is an important crop during rabi. It has a contribution of 1.51% towards national production from 1.07 % of wheat-growing area of the country with a productivity of 1.9 tonnes/ha. This is due to the fact that wheat in hills is mainly rainfed as compared to irrigated crop in the tarai. The total area under wheat is 0.4 million ha, with a total production of 0.8 tonnes and productivity of 1.9 tonnes/ha, over the last five years. The constraints are water scarcity in hills and Tarai areas, low soil organic carbon status, high nutrient mining, imbalanced fertilization, and infestation of powdery mildew and Karnal bunt diseases.
    </p>
    
