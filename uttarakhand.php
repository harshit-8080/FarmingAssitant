    <h1>Region Wise Variation</h1>
    
    <p>
       The growth of foodgrain production is quite variable in different areas. As a result, agriculture scenario presents a mixed picture. (Area Production and Productivity 2010-11) Productivity of district Udham Singh Nagar, Haridwar, Nainital (plain) and Dehradun (plain) is very high, on the other side; productivity of the hilly area is very low, although the valleys are fertile. Plains and hill agriculture stand in stark contrast to each other. While productivity in plains can be compared with agriculturally developed regions of the country, productivity in hill lags far behind. The Green Revolution had highly benefitted the farming system of the plain area of the State while it has neglected the hilly region.
    </p>
    
