<p style="font-size: 50px;margin-left: 30px;">Introduction</p>
<div id="d1"   style="font-size:20px"> Mizoram is a state in the northeast of India. Mizoram is considered by many as a beautiful place due to its dramatic landscape and pleasant climate. There have been many attempts to increase revenue through tourism but many potential tourists find the lack of amenities to be a hurdle. However, the State continues to promote itself and many projects have been initiated. The tourism ministry continues to maintain or upgrade its tourist lodges throughout the state. Foreign tourists are required to obtain an 'inner line permit' under the special permit before visiting. The permit can be obtained from Indian missions abroad for a limited number of days or direct from Mizoram Government authorities within India. The state is rich in bird diversity, which has the potentiality to make it a major birding destination.[3] For Mrs Hume's pheasant (Syrmaticus humiae), Mizoram is a stronghold.[4] There is also a rare record of the wild water buffalo from the state.[5] There are several past records of the Sumatran rhinoceros from Mizoram, then Lushai Hills.[6] The small population of wild elephants can be seen in Ngengpui and Dampa Sanctuaries.[7] Some of the interesting sites are Mizo Poets' Square also known as Mizo Hlakungpui Mual in Mizo, the Great Megaliths locally known as 'Kawtchhuah Ropui'.</div>
