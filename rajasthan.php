		The diversity of land, soil and climate in Rajasthan makes the state a distinctly different one
in India. In two major seasons, viz., Kharif and Rabi, cultivation in the state under normal
conditions of rainfall grow mostly cereals, oil seeds and pulses. However, prospects of a normal
