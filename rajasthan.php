<h2>
	Soil And Vegetation In MadhyaPradesh
</h2>
<div id="shyam">
<p>

The soil and vegetation of the state of Rajasthan varies with its wide-ranging topography of arid plains or parched regions, hilly tracts of the Aravalis, the flood prone plains of Eastern Rajasthan and the oldest range of fold mountains- Aravali Hills.

The type of soil and vegetation vary with the availability of water. Interestingly some evidence testifies that Rajasthan was once filled with lush green vegetation and had numerous streamlets running through the land. But those days have elapsed and presently a major portion of Rajasthan is desiccated. This is due to the continuous soil erosion and the recurrence of droughts. The sandy soil has very less capacity to store water. 

</p>

<img src="https://www.mdpi.com/water/water-07-05547/article_deploy/html/images/water-07-05547-g001.png" class="pho"/>

<p>
The ground water level is very low due to over-exploitation and the limited, erratic rainfall averaging 360mm per year allows the growth of scanty, shrub -like thorny vegetation. Grasses and species like P. cineraria grow in the regions where little water is available. Water level is available only at a depth of 30 to 61m. However food crops are grown in the plains that are drained by the rivers and streamlets owing to the alluvial and clay soil deposits. The hilly tracts of the Aravali are characterized by the black, lava soils that sustain the growth of cotton and sugarcane.

The types of soil available in Rajasthan are mostly sandy, saline, alkaline and chalky (calcareous). Clay, loamy, black lava soil and nitrogenous soils are also found.
</div>
 <h2>Rajasthan Climate</h2>
 <div id="shyam">
 	<p>
 	Rajasthan lies to the north west of India, just above the Tropic of Cancer. The climate of Rajasthan greatly varies throughout the state. In the west, Rajasthan is relatively dry and infertile; this area includes some of the Thar Desert, also known as the Great Indian Desert. The Aravalli Hill ranges tower over the Thar Desert. They are famed for the hill station of Mount Abu that is home to the famous Dilwara Jain Temples. The presence of the desert makes the summer days very hot and nights very cold. Nighttime temperatures in winters can reach -1°C, as happened recently in Churu district in north Rajasthan. Jaisalmer and Barmer districts also lie in the western Rajasthan, and every winter tourists flock to these districts to take camel rides on the shifting sand dunes. Jaisalmer is known for its peculiar yellow sandstone, which was used to construct the "golden" Jaisalmer Fort.</p>
 	<img src="https://weather-and-climate.com/uploads/average-temperature-india-jodhpur-fahrenheit.png" class="pho"/>
<p>
 	 The maximum daytime temperatures in summers can reach 45°C. However, winters in Rajasthan are more or less pleasant, and is the best time to visit this state.The southern districts of Udaipur and Chittor are more or less fertile. Infact, Udaipur is known as the Lake City because of its many lakes and canals that reminds one of Venice. The Mewar region, also to the south of the state, was the scene of many battles during the Mughal rule.
The Chambal River runs through the rocky northeastern region. The deep gullies and ravines of Chambal were once hideouts of the infamous bandits of Chambal. However, now they are a quiet tourist destination for people from around the country.

