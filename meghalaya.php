<div id="d1"   style="font-size:20px"> The state is bounded to the south by the Bangladeshi divisions of Mymensingh and Sylhet, to the west by the Bangladeshi division of Rangpur, and to the north and east by India's State of Assam. The capital of Meghalaya is Shillong. During the British rule of India, the British imperial authorities nicknamed it the "Scotland of the East".[7] Meghalaya was previously part of Assam, but on 21 January 1972, the districts of Khasi, Garo and Jaintia hills became the new state of Meghalaya. English is the official language of Meghalaya. The other principal languages spoken include Khasi, Garo, Pnar, Biate Hajong, Assamese and Bengali. Unlike many Indian states, Meghalaya has historically followed a matrilineal system where the lineage and inheritance are traced through women; the youngest daughter inherits all wealth and she also takes care of her parents.[7]
Traditional agriculture is often considered a step between the local hunt- and-gather practice, which provides communities with subsistence levels of food, and the practices of modern agriculture, used for mass-production of food for global distribution. This traditional agriculture practice develops a balance between meeting our present needs, conserving natural resources, and protecting the environment for the benefit of future generations. Traditional agricultural approaches are not practical for mass food production, but accounts for a substantial amount of local food production in the developing world1.
