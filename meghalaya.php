<div id="d1" style="font-size: 20px"> Climate. The climate of Meghalaya is generally mild. In August the mean temperature at Shillong (in the Khasi Hills) is in the low 70s F (about 21–23 °C); it falls to the upper 40s F (about 8–10 °C) in January. ... During the winter months (December to February), the climate is relatively dry.</div>



