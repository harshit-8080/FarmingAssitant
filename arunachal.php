<p style ="font-size: 50px;margin-left: 30px;">Climate of Arunachal pradesh</p>
<div id="d1" style="font-size: 20px"> The weather and the climate of Arunachal Pradesh are quite distinct from the rest of the country. The climate of the state is dominated by the Himalayan system and the altitudinal variations. The climate is highly hot and humid at the lower altitudes and in the valleys covered by swampy dense forest particularly in the eastern section, while it becomes exceedingly cold in higher altitudes. The rainfall of Arunachal Pradesh in amongst the heaviest in the country.</div>



