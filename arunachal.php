<p style="font-size: 50px;margin-left: 30px;">Introduction</p>
<div id="d1"   style="font-size:20px">Agriculture is the main source of earning of Arunachal Pradesh. The state gets as much as 500 centimeters of rainfall and this accounts for the large number of rivers and lakes in the region apart from extensive forests which have induced the people to adopt the 'Jhum' form of cultivation in which a select area of the forests is burnt and used for cultivation. In the following years the farmers shift to another forest plot which leads to the cultivation being called shifting cultivation. About 53% of the total cultivated area is under Jhum and the rest under permanent cultivation. Due to good rain rice the major crop. Other important crops are maize, millet, wheat, pulses, potato, oil seeds and sugarcane. Ecological conditions of Arunachal Pradesh are congenial for the growth of horticulture. Besides this there are plenty of oranges, guavas, pineapples, lemon, litchi, papaya, and temperature fruits like apple, plum, pear, peach, cherries, walnut, almonds are grown. 
Various steps were taken to diversify the agriculture economy by encouraging the cultivation of cash crops like potatoes, and horticulture crops like apple, oranges, guavas, and pineapples, etc. Many important projects such as Regional Seed Foundation Potato Farm at Tawang, Regional Apple Nursery at Dirang, and State Horticulture Farm at Sheragaon were set up with the help of North-Eastern Council to boost agro-horticulture activities. Apart from these Gramsevak Training Centre and farmers Training Centre located at various parts of the state impart training in scientific methods of rural development and agriculture respectively. In this way pre-agricultural level of technology in 1950 is now firmly established in the world of hybrid varieties, genes and biotechnology.</div>
