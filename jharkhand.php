<h2>
	soil and vegetation in Jharkhand
</h2>
<div id="shyam">
<p>
	The cultivated area of the state is about 1.8 million ha, comprising 22% of the geographical area. The net irrigated area is about 0.16 million ha, constituting 9.3% of the cultivated area. Being largely rainfed, the state has a cropping intensity of 126%. For higher agriculture production, major constraints of the state are sloping lands with hard rock areas, severe soil erosion, water scarcity, acidic soils, low soil fertility, low input use of agricultural inputs and open grazing in rabi season.</p>
