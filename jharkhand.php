 <h2>Jharkhand Climate</h2>
 <div id="shyam">
 	<p>Climate of Jharkhand varies from Humid subtropical in the north to tropical wet and dry in the south-east.[47] The main seasons are summer, rainy, autumn, winter and spring. The summer lasts from mid-April to mid-June. May, the hottest month, characterized by daily high temperatures around 38 °C(100 °F) and low temperatures around 25 °C (77 °F). The southwest monsoon, from mid-June to October, brings nearly all the state's annual rainfall, which ranges from about 40 inches (1,000 mm) in the west-central part of the state to more than 60 inches (1,500 mm) in the southwest.</p>
