<h2>
	Important Agriculture Crops
</h2>
<div id="shyam">
	<p>
		The state has three main crop seasons: kharif (or aghani), rabi, and garma (summer). The major kharif crops are rice, millet and maize. The rabi crops are wheat, pulses, gram, and mustard. In the garma, farmers grow rice, maize, groundnuts and vegetables.
		Pulses are non-traditional crops but are fast emerging as important crops in the state, with the area under them doubling from 2009-10 to 2012-13 to reach about 600,000 hectares. Together with partners like the Jharkhand Tribal Development Society (JTDS) and Birsa Agricultural University (BAU), ICRISAT research is promoting farmer-preferred improved chickpea varieties to raise average yields from the current low of 830kg/ha and help design resilient productive cropping systems.</p>

 		<img src="https://image.slidesharecdn.com/organic-weed-managementgroupno13-150327015328-conversion-gate01/95/organic-weedmanagement-15-638.jpg?cb=1427421320" class="pho"/>


Improved crop production technologies for chickpea and oilseed crops are being promoted in rainfed rice fallow lands where farmers used to grow nothing after the paddy harvest. Chickpea and other crops like field pea, linseed, or mustard can now use the remaining soil moisture. Since there is a thriving market for these foodgrains, farmers are quickly adopting these new crops.
	</p>
