<p style="font-size: 50px;margin-left: 30px;">Introduction</p>
<div id="d1"   style="font-size:20px"> Agriculture is the main occupation of about 62 per cent of the people in Andhra Pradesh. Rice is a major food crop and staple food of the State contributing about 77 per cent of the food grain production. Other important crops are jowar, bajra, maize, ragi, small millets, pulses, castor, tobacco, cotton and sugarcane. Forests cover 23 per cent of the State's area. Important forest products are teak, eucalyptus, cashew, casuarina, bamboo, softwood, etc. The production of food grains in the state for the first time since formation of the State is recorded at 204.04 lakh tonne in 2008-09 as against 198.17 lakh tonne in 2007-08.

The Government is working with a mission of "Sustainable Agriculture Production with minimum cost of cultivation, eventually enhancing the return on income to the farmer". In the process of making the mission a reality, the Government is implementing schemes for the welfare of farmers like 9 hours free power supply, subsidized seed, subsidized interest rate on crop loans, subsidized and quality agricultural inputs etc., Andhra Pradesh occupies the first position in respect of agricultural loans from commercial and cooperative banks.

The debt waiver scheme of Central Government and the incentive scheme of the State Government have helped about one crore farmers in the state to the tune of Rs.16,000 crore. The Government of Andhra Pradesh is also keen on introducing cooperative farming to set up farm yields.</div>
