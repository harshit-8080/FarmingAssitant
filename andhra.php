<p style ="font-size: 50px;margin-left: 30px;">Climate of Andhra pradesh</p>
<div id="d1" style="font-size: 20px"> The climate of Andhra Pradesh varies considerably, depending on the geographical region. Summers last from March to June. In the coastal plain, the summer temperatures are generally higher than the rest of the state, with temperature ranging between 20 °C and 41 °C. July to September is the season for tropical rains. About one third of the total rainfall is brought by the northeast monsoon. October and November see low-pressure systems and tropical cyclones form in the Bay of Bengal which, along with the northeast monsoon, bring rains to the southern and coastal regions of the state.

November, December, January, and February are the winter months in Andhra Pradesh. Since the state has a long coastal belt the winters are not very cold. The range of winter temperature is generally 12 °C to 30 °C. Lambasingi in Visakhapatnam district is the only place in South India which receives snowfall because of its location as at 1,000 m (3,300 ft) above the sea level. It is also nicknamed as the Kashmir of Andhra Pradesh and the temperature ranges from 0 °C to 10 °C.</div>



