 <h2>Bihar Climate</h2>
 <div id="shyam">
 	<p>
 		Bihar climate is in accord with the climate prevailing in other parts of the Indian sub continent. The place enjoys extreme cold with rains and intense summer season as well. This is what makes Bihar rich in its climatic patterns. Therefore, there is every kind of weather for everyone. If you love winter season for traveling, it is available in Bihar. Similarly if it is in the summers you enjoy traveling, Bihar offers you that as well. It just depends on the months you know for your traveling schedule.</p>
<img src="https://upload.wikimedia.org/wikipedia/commons/thumb/a/a7/India_annual_rainfall_map_en.svg/800px-India_annual_rainfall_map_en.svg.png" class="pho"/>
<h3>November to March – Winter Season</h3>
<p>
The cold weather commences early in November and continues until the middle of the March. It is during this time, the temperatures hovers around 8 degree Celsius to 10 degrees. January is the coldest month, however. This is the best time especially if you want to experience the culture of Bihar while walking in its vicinities. This is the best time when you can roam around in its lanes and by lanes as the climate perfectly gels with your walking plan.
</p>
<img src="https://bihar.pscnotes.com/wp-content/uploads/2017/10/Agro-Climate.png" class="pho"/>
<h3>March to June – Hot Weather</h3>
<p>
Commencement of hot weather is from mid March onwards. It continues until mid June. During this time, it is extremely hot in Bihar. This is the time when usually people stay indoors. However, if one wishes to go for exploring the vicinities of Bihar, one has to prepare, which includes taking ample of drinking water to avoid dehydration and related problems.
</p>
<h3>June to September – Rainy Season</h3>
<p>
Soon after the scorching sun hits the state, rainy season continues until mid September. The beginning of this weather is when a storm arises from the Bay of Bengal. It is also a good time to visit Bihar as crops mesmerizes your soul with their lush green appearance. The temperature remains moderate usually in this season. For those who love to walk in rain, this is the best time to visit Bihar, as there is ample of opportunity for them.</p>

