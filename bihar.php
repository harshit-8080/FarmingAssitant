</h2>
<div id="shyam">
	<p>
Rice, wheat, and maize are the major cereal crops. Arhar, urad, moong, gram, pea, lentils, and khesaria are some of the pulses cultivated in Bihar. Bihar is the largest producer of vegetables.Total vegetable production in Bihar is about 156.29 lakh onnes.Potato,Onion,Tomato,Brinjal,Okra and Cauliflower is the major vegetable crop of the state. Bihar is known all over India for its litchi and mango. The four most important fruit crops in Bihar are mango,guava, litchi and banana. Flower production in Bihar has increased recently, providing immense opportunity of employment and income in rural areas of Bihar. In 2013-14, about 99 tonnes of rose, 6799 tonnes of marigold, 317 tonnes of jasmine (Bela)and 536 tonnes of the tuberose were produced in Bihar.

