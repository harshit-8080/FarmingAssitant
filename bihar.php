<h2>
	Soil And Vegestation in Bihar
</h2>
<div id="shyam">
<p>
Bihar is situated on the World's fertile alluvial plain of Gangatic Valley which extends from the foothills of the Himalayas in the north to a few miles south of the river Ganges as it flows through the State from the west to the east. Variation in relief, precipitation, vegetation and parents rocks have resulted various soil variety due to the region relief features.
</p>
<p><b>Types of soil, which are usually found in the state are:</b></p>
<h3>Soils of Northern Plain</h3>
<p>
Soil of this region is formed due to the deposition sediments brought by Gandak, Burhi Gandak, Mahananda, Kosi and Saryu rivers. Hence, the soil is further divided into three types:</p>

<h3>1. Piedmont Swamp Soil:</h3><p>It is swallowing to deep over bed rocks and pebbles. It is mostly clayey, rich in organic matter and neutral reaction. This type of soil is found in North-Western Chamaparan district.</p>

<h3>2. Terai Soil:</h3><p>It is grey to yellow in colour and neutral to moderately acidic in reaction. The soil of Terai is more fertile than upland terai. This is types of soil is found in the northern part of Bihar near by the border of Nepal and foothill of Himalaya.</p>

<h3>3. Gangetic Alluvium Soil:</h3><p>This soil is generally fertile in Bihar plain but to regular tillage without application of manure have costs low organic content. It is mostly loamy with varying thickness. It is thinner towards south and thicker towards north.</p>

<img src="http://www.brandbharat.com/images/bihar/bihar-soil-map.jpg" class="pho"/>

<h3>Soils of Southern Plain</h3>
<p>
This type of soil is found between North Ganga plain and Southern plateau and it formed due to the alluvium deposited by Sone, Punpun, and Falgu Rivers.</p>

<h3>1. Karail-Kewal Soil: </h3>
<p>
	It is heavy clay soil (alkaline feature) which is characterised by wet paddy lands or dhandhar. It is found in Rohtas to Gaya, Aurangabad, Jahanabad, Munger and Bhagalpur. It is best suited for rice and rabi crops like wheat, linseed, pulses and gram.
</p>

<h3>2. Tal Soil:</h3>
 <p>
	 It is found in backwater belt of Ganga which is spread from Buxar to Banka district. The colour of the soil varies from light grey to dark grey and in texture from medium to heavy soil. Rabi or spring crops like wheat, khesari, gram, peas, masoor, and lentsil are harvested after water dries up and their yield is quite high.
</p>
<h3>3. Balthar Soil:</h3>
 <P>
 	It is less fertile and has less water absorption capacity. Maize, jowar, bajra, and gram are chief crops. It is found in Kaimur plateau to Rajmahal hills in narrow belt.
 </P>
