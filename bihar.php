<h2>Introduction</h2>
<div id="shyam">
<p>
	Agriculture is the key to the overall development of the State economy.  Agriculture is the backbone of Bihar's economy 77% of workforce and generating nearly 24.84% of the State Domestic Product. The percentage of population employed in agriculture production system in Bihar is estimated to 77%, which s much higher than the national average. Nearly 24.84% of GDP of the state (2011-12) has been from agriculture sector (including forestry and fishing).The state has attained self sufficiency in food grains production. Barring maize and pulses productivity of various farm produce in Bihar is much below the national average. Though the area under cultivation is shrinking, there is tremendous scope for income generation, by improving productivity. Adverse climatic condition, like draught and floods, do play a role in decreasing products.But these adverse conditions can be overcome to some extent by irrigation, flood control and drainage schemes.</p>
<img src="https://images.yourstory.com/cs/wordpress/2016/08/yourstory-kedia-bihar.jpg?fm=png&auto=format?fm=png&auto=format" class="pho"/>
<p>
	  The agriculture production can only be increased to some extent through enhanced cropping intensity, change in cropping pattern, improvement in seeds of high yielding varieties, cultivation practices and with the availability of better post harvest technology etc. State Govt. is trying to re-orient agriculture through diversification policy and other measures.
	Agriculture is the single largest private sector occupation in Bihar. The goal of the agriculture production system should be to maximize income of land owing and landless rural populace to improve their livelihoods. The vulnerability to income and consumption shocks makes it imperative to develop formal agriculture insurance mechanisms to cope with such risks.
</p>
