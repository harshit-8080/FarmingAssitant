<h2>
	Soil And Vegetation in MadhyaPradesh
</h2>
<div id="shyam">
<p>
According to the 2011 figures, the recorded forest area of the state is 94,689 km2 (36,560 sq mi) constituting 30.72% of the geographical area of the state.[20] It constitutes 12.30% of the forest area of India. Legally this area has been classified into "Reserved Forest" (65.3%), "Protected Forest" (32.84%) and "Unclassified Forest" (0.18%). Per capita forest area is 2,400 m2 (0.59 acres) as against the national average of 700 m2 (0.17 acres). The forest cover is less dense in the northern and western parts of the state, which contain the major urban centres. Variability in climatic and edaphic conditions brings about significant difference in the forest types of the state. In January 2019 1.5 million volunteers in the state planted 66 million trees in 12 hours along the Narmada river.[21]
</p>

