<h2>Introduction</h2>
<div id="shyam">

<p>
	Madhya Pradesh, with its large area, enjoys diverse climatic and soil conditions suitable for a broad range of agricultural products. Agriculture sector in Madhya Pradesh forms the backbone of its economy. It contributes almost one-fourth of the Gross State Domestic Product (GSDP) and is the main source of employment for over 65 percent of the population and constitutes about 60- 75 percent of the rural income.The state is a large producer of soybean and wheat. Sharbati variety of famous wheat is grown in Sehore, Vidisha and Ashok Nagar districts, and in some parts of Bhopal and Hoshangabad.Madhya Pradesh leads in the production of gram, linseed, green pea, garlic and coriander. The state is also a major producer of linseed, mustard, sunflower and safflower.</p>

