 <h2>MadhyaPradesh Climate</h2>
 <div id="shyam">
 	<p>
 	Madhya Pradesh has a subtropical climate. Like most of north India, it has a hot dry summer (April–June), followed by monsoon rains (July–September) and a cool and relatively dry winter. The average rainfall is about 1,371 mm (54.0 in). The southeastern districts have the heaviest rainfall, some places receiving as much as 2,150 mm (84.6 in), while the western and northwestern districts receive 1,000 mm (39.4 in) or less.Usually we can't find extreme temperatures in Madhya Pradesh but still one can experience every season here like summers with heat waves, winters with cold waves and monsoon with heavy rainfall.
 	</p>
