<p style="font-size: 50px;margin-left: 30px;">Introduction</p>
<div id="d1"   style="font-size:20px">Gujarat has long been considered as one of the most progressive states of India, be it on the industrial front or the agricultural front. Imbued with a proactive, visionary outlook, its government has catalyzed development in various spheres the collective effort has, thus, propelled the state towards progress. Gujarat Agro Industries Corporation (GAIC), which promotes agricultural activities at the ground level and fosters the development of agro industries in the state, is a manifest example of the forward-looking policies of the state. Set up in 1969 under the Companies Act, 1956, GAIC acts as a facilitator and nodal agency for implementation of various scheme of Government of Gujarat and Government of India

The present-day globalized agricultural environment necessitates a holistic approach to farming and all related activities. Moreover, strengthening of the forward and backward linkages between agriculture and industry has also become a prerequisite. This is where GAIC steps in. It plays an important role in increasing competitiveness by encouraging farmers to adopt an integrated approach to agriculture and work towards better returns from the existing set-up</div>
