<p style ="font-size: 50px;margin-left: 30px;">Climate of Gujrat</p>
<div id="d1" style="font-size: 20px">The climate of Gujarat involves diverse conditions. The winters are mild, pleasant, and dry with average daytime temperatures around 29 °C (84 °F) and nights around 12 °C (54 °F) with 100 percent sunny days and clear nights. The summers are extremely hot and dry with daytime temperatures around 49 °C (120 °F) and at night no lower than 30 °C (86 °F). In the weeks leading up to the arrival of the monsoon rains the temperatures are similar to above but with high humidity which makes the air feel hotter. Relief when the monsoon season starts around in mid June. The day temperatures are lowered to around 35 °C (95 °F) but humidity is very high and nights are around 27 °C (81 °F). Most of the rainfall occurs in this season, and the rain can cause severe floods. The sun is often occluded during the monsoon season. Though mostly dry, it is desertic in the north-west, and wet in the southern districts due to a heavy monsoon season.</div>



