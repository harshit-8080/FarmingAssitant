	<div id="ram">	<h1>Overview</h1>
		<p>With Sahyadri ranges running along the coast of Maharashtra, the state is geographically divided into two regions; the Konkan coastal plains which are paddy fields and coconut gardens and the great river basins formed by the rivers flowing out from the Western Ghats (Sahyadri). Agriculture is the mainstay of the state of Maharashtra. It is the main occupation of the people. Almost 82% of the rural population depends on agriculture for livelihood. Both food crops and cash crops are grown in the state. The main food crops of Maharashtra are mangoes, grapes, bananas, oranges, wheat, rice, jowar, bajra, and pulses. Cash crops include groundnut, cotton, sugarcane, turmeric, and tobacco. </p>

