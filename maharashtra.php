	 <h1>Irrigation</h1>
		<p>
			The total irrigated area which has been used for crop cultivation is 33, 500 square kilometres; that makes it to only 18 per cent of Gross Cropped Area. Cashing in on its strengths, Maharashtra state has been focussing on high-value Horticulture rather than staple crops. Large areas of the state have been brought under fruit and vegetable cultivation which present tremendous export potential and have added to the treasury of the state. Agri Export Zones have been developed to market the produce. 
		</p>
		<p>
			Maharashtra is the first state to adopt<b> 'Dry Land Farming Technology'</b> with the mission to help its farmers. Farmers are being trained on the new techniques for farming, pond irrigation, micro irrigation, inter-cropping, double cropping, etc., in addition to providing them with improved short duration yield seeds, fertilizers, pesticides etc, This technology transfer from the 'Agricultural Universities' to the farmers is evident from the high yields.

			The state is also a pioneer in co-operative water user associations; it stands first in the country with more than <b>60% </b>of the area under drip irrigation. Efficient management of rain water and soil conservation is done to boost agricultural production from dry lands.
		</p>
	</div>
	<div id="ram">
		<h1>Rainfall & It's Impact</h1>
		<p>The state experiences tropical conditions and rainfall is particularly concentrated to the Konkan and the hilly Sahyadri region. The rain pattern differs from region to region. Central Maharashtra experiences scattered rainfall almost one-third area receives scanty rainfall and is always in the grip of water scarcity and drought. <b>Per Maharashtra state Agriculture Portal, the state has 24 per cent of drought-prone area of the country. Per reports, as high as ninety-nine talukas in the state are chronically drought affected.</b></p>

		<img src="http://im.rediff.com/money/2015/may/12climate.jpg" class="pic">
		<p>Topography and the geology of the region is such that groundwater resources are limited and in groundwater rich areas, over-extraction of groundwater has been observed with emphasis on cash crops such as sugarcane, bananas, grapes, and oranges. </p>

