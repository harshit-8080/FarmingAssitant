		<p>
			The main food crops of Maharashtra are <b>mangoes, grapes, bananas, oranges, wheat, rice, jowar, bajra, and pulses</b>. Cash crops include <b>groundnut, cotton, sugarcane, turmeric, and tobacco</b>. In recent years Maharashtra has become the largest producer of <b>Onions (63%), Banana (75%), Mandarin oranges(75%), Tomatoes(42%), Pomegranates, Alphonso Mangoes (90%)</b> and <b>seedless grapes (78%)</b>. The state leads the sugar industry sector with about 150 productive cooperative sugar mills. 
