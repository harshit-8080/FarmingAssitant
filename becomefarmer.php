<h2 style="color: blue">Some Common Myths About Farming In India:</h2>
<h2>Myth 1: Indian agriculture is on a permanent decline</h2>
<p class="para">Reality: The Report says, Indian agricultural production increased from $87 billion in 2004-05 to $322 billion in 2015-16, marking a 12.6 per cent annual growth rate. Another fact is that India ranks second in the world in agriculture production whereas the countryâ€™s world rank in services and industry sector is 11th and 12th respectively. </p>
<h2>Myth 2: Indian agriculture is food-grains centric.</h2>
<p class="para">Reality: Fruits, vegetables and animal husbandry contribute to 60 per cent of India's agricultural GDP. India is the world's largest milk producer, the second largest producer of fruits and vegetables, and the third largest egg and poultry producer.</p>
<h2>Myth 3: Declining size of Indian farmland leads to low production.</h2>
<p class="para">Reality: Globally, the size of farm holdings less than two hectares is 84 per cent compared to China's 98 per cent, and India's 72 per cent. Despite low farm sizes, India and China are the world's leaders in agricultural production.</p>
<h2>Myth 4: Indian agriculture is heavily dependent on rainfall</h2>
<p class="para">Reality: Indian agriculture is more monsoon-resilient than assumed. India has the world's largest area 91 million hectare under irrigation. Over 70 per cent of wheat land and 59 per cent paddy in India are irrigated.Eight top horticulture crops grown in India also have high levels of irrigation. China and the US with lower precipitation (rain and snow) are among the world's top agricultural producers.</p>
