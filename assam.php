 <p style ="font-size:20px;">Assam is known for its extensive forest areas and availability of rich floras and faunas besides other valuable forest products. As per information available from the State Forest Department, the total area under forests in Assam was 26,781.91 Sq.km. at the end of March, 2003, out of which 15,492.329 Sq. Km. was under Reserved Forests and 2,860.942 Sq. Km. under Protected Forest Area. But this rich forest cover and valuable forest resources of the state are disappearing rapidly particularly from the last few decades due to massive deforestation, illegal felling, forest fragmentation, encroachment in the fringe areas, poaching, biopiracy and other unplanned development activities.

