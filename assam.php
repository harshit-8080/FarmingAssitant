<p style="font-size: 50px;margin-left: 30px;">Introduction</p>
<div id="d1"   style="font-size:20px">Agriculture in Assam accounts for about 54.11 per cent of the total geographical area of the state, with almost 70% of the population directly dependent on agriculture as an income source. It is an integral revenue earning sector of the state, sustaining a huge number of people.
Owing to the availability of fertile soil and abundance of rain, the state provides a favourable environment of production of both food and cash crops such as rice, pulses, tea etc. Since the economy of Assam is primarily dependent on agriculture, the Agriculture Department of Assam is striving to practise a sustainable, cost-effective and scientifically advanced form of agriculture through implementation of various policies and initiatives.
Initially, the Agriculture Department in the state was created in April, 1882. But later on in 1923, three allied Department of Agriculture, Cooperation and Industries were combined. At present, the Department of Agriculture is driven by the vision to implement policies and programmes aimed at achieving rapid agricultural growth through optimum utilization of available resources of the State. It aims to educate the farmers in practising sustainable and commercial modes of agriculture, with the use of scientific methods. Besides popularizing the use of farmer's friendly bio-fertilizers, it gives utmost emphasis on creation of assured irrigation facilities to the farmers through minor irrigation schemes to obtain maximum returns from their land. The Department also conducts various programmes from time to time to enlighten the farmers on the use of ICT applications and calamity relief programmes during natural disasters.</div>
<p style ="font-size: 50px;margin-left: 30px;">Important agriculture crops of Assam</p>
<div id="d1" style="font-size: 20px"> Assam produces both food and cash crops. The principal food crops produced in the state are rice (paddy), maize (corn), pulses, potato, wheat, etc., while the principal cash crops are tea, jute, oilseeds, sugarcane, cotton, and tobacco.
