<p style ="font-size: 50px;margin-left: 30px;">Climate of Assam</p>
<div id="d1" style="font-size: 20px">With the "Tropical Monsoon Rainforest Climate", Assam is temperate (summer max. at 95–100 °F or 35–38 °C and winter min. at 43–46 °F or 6–8 °C) and experiences heavy rainfall and high humidity.[55][58] The climate is characterised by heavy monsoon downpours reducing summer temperatures and affecting foggy nights and mornings in winters, frequent during the afternoons. Spring (Mar–Apr) and autumn (Sept–Oct) are usually pleasant with moderate rainfall and temperature. Assam's agriculture usually depends on the south-west monsoon rains.</div>



