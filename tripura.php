<p style="font-size: 50px;margin-left: 30px;">Introduction</p>
<div id="d1"   style="font-size:20px"> Tripura is one of the seven states in the north eastern part of India located between 22 degree and 56 minutes and 24 degree and 32 minutes north latitude and between 90 degree and 09 minutes and 92 degree and 20 minutes east latitude. It is bounded on the north, west, south and south-east by Bangladesh whereas in the east it has a common boundary with Assam and Mizoram.
Tripura has a total cultivable land of 2,80,000 hectares and irrigation potential of 1,27,000 hectares. Out of the available water resources 79,000 hectares can be brought under assured irrigation through surface water and 48,000 hectares through ground water. Total potential created upto 31st March, 2014 was 1,12,806 hectares.  

</div>
