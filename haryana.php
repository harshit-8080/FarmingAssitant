    <h1>Overview</h1>
    
    <p>
      Despite recent industrial development, Haryana is primarily an agricultural state. About 70% of residents are engaged in agriculture. Haryana is at Second position in food grain production in the country.
    </p>
    <img src="https://image.slidesharecdn.com/cainharyana-120926080221-phpapp01/95/conservation-agriculture-in-haryana-india-2-728.jpg?cb=1348647019" class="pic">
    <p>
      Haryana contributed significantly to the Green Revolution in India in the 1970s that made the country self-sufficient in food production. The state has also significantly contributed to the field of agricultural education in the country. Asia's biggest agricultural University - Chaudhry Charan Singh Haryana Agricultural University is located at Hisar , which has already made a significant contribution in ushering 'Green Revolution'. 

    </p>
