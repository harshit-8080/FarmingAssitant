    <h1>Irrigation</h1>
  
    <p>
      Haryana has a tremendous irrigation infrastructure. Irrigation in Haryana uses water either from underground or from surface through canals. Numerous canals have been dug and pumps put up to make sure that the farmer doesn't pack up and leave in times of drought. Currently in Haryana, the most important technology for groundwater irrigation is the use of tubewells with pump. About 86% of the area is arable, and of that 96% is cultivated. About 75% of the area is irrigated, through tubewells and an extensive system of canals.  
    </p>

   </div>
    
   <div id="ram">
      <h1>Major Crops</h1>
    <p>
      Wheat and rice are the major crops. Haryana is self-sufficient in food production and the second largest contributor to India's central pool of food grains. The main crops of Haryana are<b> Wheat, Rice, Sugarcane, Coton, Oilseeds, Gram Barley, Corn, Millet</b> etc.<p> There are two main types of crops in Haryana:<b> Rabi and Kharif</b>.</p></p>
      <p>The major Kharif crops of Haryana are <b>rice, lowar, bajra, maize, cotton, jute, sugarcane, sesame and groundnut</b>. For these crops the ground is prepared in April and May and the seeds are sown at the commencement of rains in June. The crops are ready for harvesting by the beginning of November.</p> 
        <p>The major Rabi crops are <b> Wheat, tobacco, gram, linseed, rapeseed and mustard</b>. The ground is prepared by the end of October or the beginning of November and the crops are harvested by March. 
      </p>
    

