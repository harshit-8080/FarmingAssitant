    <h1>Dairy Farming in Haryana</h1>
    <p> 
      Dairy farming is also an essential part of the rural economy.Haryana has a livestock population of 98.97 lakh.Milk and milk productsform an essential part of the local diet. There is the saying Desaan main des Haryana, jit doodh dahi ka khaana , which means "Among places is Haryana, where the staple food is milk and yoghear ". Haryana, with 660 grams of availability of milk per capita per day, ranks at number two in the country as against the national average of 232 grams. There is a vast network of milk societies that support the dairy industry. 
    </p>

    <img src="https://img2.exportersindia.com/product_images/bc-full/dir_152/4536816/dairy-farming-consultancy-1506427543-3357393.jpeg" class="pic">
    <p>
      The National Dairy Research Institute at Karnal, and the Central Institute for Research on Buffaloes at Hisar are instrumental in development of new breeds of cattle and propagation of these breeds through embryo transfer technology. The Murrah breed of water buffalo from Haryana is world-famous for its milk production.
