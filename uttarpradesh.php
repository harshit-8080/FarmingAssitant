<h2>
	Important Agriculture Crops
</h2>
<div id="shyam">
	<p>
		Uttar Pradesh is a major contributor to the national food grain stock. In 2013-14, this state produced 50.05 million tonnes of foodgrain, which is 18.90% of the country's total production. This is partly due to the fertile regions of the Indo-Gangetic plain and partly due to irrigation facilities such as canals and tube-wells. Lakhimpur Kheri is a densely populated sugar-producing district in the country. It has been the most common producer of food grains in India since the 1950s, due to high-yielding varieties of seed, greater availability of fertilizers and increased use of irrigation.[4] Western Uttar Pradesh is more advanced in terms of agriculture as compared to the other regions in the state. The majority of the state's population depends upon farming activities. Wheat, rice, pulses, oilseeds and potatoes are major agricultural products. Sugarcane is the most important cash crop throughout the state. Uttar Pradesh is one of the most important states in India as far as horticulture is concerned. Mangoes are also produced in the state.[5]</p>

		<img src="https://www.mapsofindia.com/maps/uttarpradesh/uttarpradesh-agriculture-map.jpg" class="pho"/>
