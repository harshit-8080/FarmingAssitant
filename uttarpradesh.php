Uttar Pradesh supports about 15% of India's total livestock population. In 1961, its livestock comprised 15% cattle, 21% buffaloes, 13% goats, and 8% other livestock. Between 1951 and 1956 there was an overall increase of 14% in the livestock population. There are about 8,000 km² of water area, including lakes, tanks, rivers, canals, and streams. The fishing area in the state is over 2,000 km² and there are more than 175 varieties of fish. </p>
