 <h2>Uttar Pradesh Climate</h2>
 <div id="shyam">
 	<p>
 	The climate of Uttar Pradesh is the tropical monsoon type, with warm weather year-round. Average high temperatures in Lucknow range from about 70 °F (low 20s C) in January to over 100 °F (38 °C) in May and June. High temperatures of about 120 °F (50 °C) have been recorded at Gonda, northwest of Faizabad.</p>
 	<img src="https://weather-and-climate.com/uploads/average-temperature-india-allahabad.png" class="pho"/>
<p>

Annual rainfall in the state ranges from 40–80 inches (1,000–2,000 mm) in the east to 24–40 inches (600–1,000 mm) in the west. About 90 percent of the rainfall occurs during the southwest monsoon, lasting from about June to September. With most of the rainfall concentrated during that four-month period, floods are a recurring problem and can cause fatalities and heavy damage to crops and property, particularly in the eastern part of the state. Periodic failure of monsoons results in drought conditions.</p>
