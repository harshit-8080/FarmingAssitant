<h2>
	Soil And Vegetation In Utter Pradesh
</h2>
<div id="shyam">
<p>
Much of the area of Uttar Pradesh is covered by a deep layer of alluvium spread by the slow-moving rivers of the Ganges system. Those extremely fertile alluvial soils range from sandy to clayey loam. The soils in the southern part of the state are generally mixed red and black or red-to-yellow.</p>

