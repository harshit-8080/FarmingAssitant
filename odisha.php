    <h1>Climate & Rainfall</h1>
    <p> 
      State’s climate is tropical, characterized by high temperature, high humidity, medium
      to high rainfall and short and mild winters. The normal rainfall of the State is 1451.2 mm. 
    </p>
    <img src="http://www.osdma.org//ckeditor/CKImages/13-Oct-2018Rainfall%20Dist_13.10.18.jpg" class="pic">
    <p>
      The actual rainfall received, vary from district to district. About 84% of rainfall is
      received during the period from June to September. Even though the quantum of rainfall is
      quite high, its distribution during the monsoon period is highly uneven and erratic. Flood,
      drought and cyclone visit regularly with varying intensity. Due to frequent occurrence of 
      3
      these natural calamities there is always reduction in the yield of Kharif rice, the major crop
      of the State. Similarly, in drought years, there is considerable loss in production of Pulses
      and Oilseeds both during Kharif and Rabi.
    </p>
    
    

