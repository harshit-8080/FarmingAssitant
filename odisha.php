   <h1>Overview</h1>
    <p>
      Agriculture in Odisha is the mainstay of majority of the populace and thus, holds the key
      to socio-economic development of the State. It suffers from frequent natural calamities like
      cyclones, drought and flash floods. The share of Agriculture Sector in the State’s GSDP has
      been declining over the years. The share of this Sector in Odisha’s GSDP is expected to be 15.6
      percent in 2013-14 as per advance estimates. Despite wide annual variations in its growth,
      the agriculture sector grew in real terms at 2004-05 prices, at a rate of 12.72 percent during
      2012-13. However, a negative growth of 3.53 percent is anticipated during 2013-14 due to
      cyclonic storm ‘Phailin’ and fl ash fl oods in the State in October 2013. This Sector still provides
      employment to more than 60% of the population directly or indirectly, making it the largest
      employment sector of the state. Hence, development in agriculture sector is vital to set the
      pace of development in the state. 
    </p>

