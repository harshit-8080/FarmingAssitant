    <h1>Irrigation</h1>
    <p>
      Out of the cultivated area of 61.80 lakh ha, about 54% is under irrigated conditions and
      46% is under un-irrigated during Kharif. 
    </p>
    <p>
      The irrigation potential created from all sources till Kharif’2013 is 39.39 lakh ha &
      in Rabi’2013-14 is 16.52 lakh ha. The gross irrigated cropped area is 35.21 lakh ha, which
      is about 70.5% of the potential created. There is a constant endeavour to enhance the
      water use effi ciency through adoption of proper water management practices in addition to
      appropriate irrigation devices.
    </p>
    
