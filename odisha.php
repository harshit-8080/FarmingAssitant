    <h1>Major Crops</h1>
    <h2 style="color: blue">Rice</h2>
    <p>
      Rice is the principal food crop in the state occupying about 40.27 lakh ha annually
      (37.48 lakh ha. during Kharif season + 2.79 lakh ha. during Rabi season). The Kharif Paddy
      area consists of 6.06 lakh ha of high land 16.51 lakh ha of medium land and 14.91 lakh ha of
      low land. The entire Rabi area is irrigated & covered by HYV Paddy where as 42% of Kharif
      Paddy area is covered under irrigation. The yield rate of rice is 2.44 tonnes/ ha as against
      national average of 2.46 tonnes / ha.
    </p>
     <h2 style="color: blue">Pulses</h2> 
     <p>
       Arhar, Mung, Biri, Kulthi, Gram, Fieldpea, Cowpea, Lentil are the pulse crops grown
      in the State. The major crops are Arhar, Mung, Biri and Kulthi. Pulses are grown mainly in
      uplands during Kharif season predominantly in inland districts & in rice fallows during Rabi
      season, mostly in coastal districts under available moisture condition. Mung & Biri are also
      grown as third crop in summer under irrigated condition. Post monsoon rains, mostly govern
      the Rabi coverage of pulses in rice fallows.
        
     </p>
