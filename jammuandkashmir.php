<div id="d1"   style="font-size:20px"> The practice of cultivating the soil in order to produce crops and domestica­tion of animals and pastoral farming are known as agriculture. The agricultural processes of a region are directly controlled by the prevailing physical environmental condition (temperature, precipitation, terrain, soil etc.) and the socio-cultural milieu (land tenancy, size of holding, technology, workforce, family requirements, irrigation, power, roads, marketing, aspira­tions of the growers, etc.).

The present article gives a concise account of the general land-use, cropping pattern, crops concentration, crops combinations and the agricultural operations being carried out in the different agro-cli­matic regions of the Jammu and Kashmir state.</div>
